#!/usr/bin/env bash
# aar_profile_snapshot_smoke.sh <repo-root> — behavior smoke for aar_profile_snapshot.sh (#469).
#
# Deterministic, fully offline: exercises the snapshot/check round-trip that closes the #469 incident
# (three closed experiments silently missed the viewer-publish leg because nothing ever wrote or checked
# a START.md instance-profile snapshot) — write, idempotent re-write, staleness detection, fail-closed on
# a missing/unknown-schema profile, the [recipes.viewer]-optional (manifest-only) path, and a
# tampered-snapshot presence mismatch. Also asserts the design-experiment and log-experiment copies stay
# byte-identical (checks.sh's own drift check duplicates this at the repo level; this smoke additionally
# proves the copy under test actually behaves correctly, not just that the bytes match). Exit non-zero on
# any failure.
set -uo pipefail
ROOT=${1:?repo root}
SCRIPT="$ROOT/plugins/experiment-lifecycle/skills/design-experiment/scripts/aar_profile_snapshot.sh"
COPY="$ROOT/plugins/experiment-lifecycle/skills/log-experiment/scripts/aar_profile_snapshot.sh"

fail=0
err(){ echo "  SMOKE-FAIL: $*" >&2; fail=1; }
ok(){ echo "  smoke ok: $*" >&2; }

[ -f "$SCRIPT" ] || { echo "  SMOKE-FAIL: helper missing: $SCRIPT" >&2; exit 1; }
[ -f "$COPY" ] || { echo "  SMOKE-FAIL: log-experiment copy missing: $COPY" >&2; exit 1; }
cmp -s "$SCRIPT" "$COPY" && ok "design-experiment and log-experiment copies are byte-identical" \
  || err "design-experiment and log-experiment copies have drifted"

T=$(mktemp -d "${TMPDIR:-/tmp}/snapsmoke.XXXXXX") || { echo "  SMOKE-FAIL: mktemp failed" >&2; exit 1; }
cleanup(){ rm -rf "$T"; }
trap cleanup EXIT
export XDG_CONFIG_HOME="$T/xdg"   # per-test profile home (isolated from any REAL instance profile on this box)
export AAR_PROFILE=""

fresh_start() {
  cat > "$1" <<'EOF'
# START.md — smoke-exp

## Your one job
do a thing

## Instance profile (snapshot)
<!-- generated by `aar_profile_snapshot.sh snapshot` — do not hand-write -->

```toml
# not yet resolved
```

## Constraints
touch only your work dir
EOF
}

complete_profile() {
  cat > "$1" <<'EOF'
schema_version = 1
[github]
research_repo = "owner/research-lab"
base_branch = "main"
branch_prefix = "run/"
private = true
[recipes.viewer]
kind = "repo"
repo = "owner/viewer-repo"
path = "recipes/viewer.md"
git_ref = "abc1234"
EOF
}

viewerless_profile() {
  cat > "$1" <<'EOF'
schema_version = 1
[github]
research_repo = "owner/research-lab"
base_branch = "main"
branch_prefix = "run/"
private = true
EOF
}

# 1. snapshot on a complete profile writes the block with [recipes.viewer] present; check then passes.
mkdir -p "$T/xdg/experiment-lifecycle"
complete_profile "$T/xdg/experiment-lifecycle/aar-profile.toml"
fresh_start "$T/START1.md"
if out=$(bash "$SCRIPT" snapshot "$T/START1.md" 2>&1) && printf '%s' "$out" | grep -q 'recipes.viewer\] present'; then
  ok "snapshot writes the block and reports [recipes.viewer] present"
else
  err "snapshot did not succeed / report viewer presence: $out"
fi
if grep -q 'profile_sha256 = "' "$T/START1.md" && grep -q '\[recipes.viewer\]' "$T/START1.md"; then
  ok "written block carries profile_sha256 and [recipes.viewer]"
else
  err "written block is missing expected fields"
fi
if out=$(bash "$SCRIPT" check "$T/START1.md" 2>&1); then
  ok "check passes immediately after snapshot"
else
  err "check failed right after snapshot: $out"
fi

# 2. re-snapshot is idempotent: exactly one heading, still passes check.
bash "$SCRIPT" snapshot "$T/START1.md" >/dev/null 2>&1
n=$(grep -c '^## Instance profile (snapshot)' "$T/START1.md")
[ "$n" = 1 ] && ok "re-snapshot replaces in place (exactly one heading)" || err "re-snapshot duplicated the heading (found $n)"

# 3. staleness: edit the live profile after snapshotting -> check BLOCKs, naming both hashes.
sed -i.bak 's/abc1234/9876543/' "$T/xdg/experiment-lifecycle/aar-profile.toml"
if out=$(bash "$SCRIPT" check "$T/START1.md" 2>&1); then
  err "check passed despite the live profile changing after the snapshot (staleness not detected): $out"
else
  case "$out" in *"is stale"*"re-run"*) ok "staleness after a profile edit BLOCKs with a re-run pointer";;
    *) err "staleness BLOCK message unexpected: $out";; esac
fi
mv "$T/xdg/experiment-lifecycle/aar-profile.toml.bak" "$T/xdg/experiment-lifecycle/aar-profile.toml"

# 4. no profile at all -> snapshot fails CLOSED, never silently writes a manifest-only block.
fresh_start "$T/START2.md"
if out=$(XDG_CONFIG_HOME="$T/xdg-truly-empty" bash "$SCRIPT" snapshot "$T/START2.md" 2>&1); then
  err "snapshot succeeded with no instance profile present at all"
else
  case "$out" in *"no instance profile found"*) ok "missing profile fails snapshot CLOSED";;
    *) err "missing-profile failure lacked the expected message: $out";; esac
fi
grep -q '# not yet resolved' "$T/START2.md" && ok "placeholder left untouched on a fail-closed snapshot" \
  || err "START.md was mutated despite the snapshot failing"

# 5. unknown schema_version MAJOR -> BLOCKs (refuse-unknown-MAJOR), even with an otherwise-complete profile.
cat > "$T/xdg/experiment-lifecycle/aar-profile-v2.toml" <<'EOF'
schema_version = 2
[github]
research_repo = "owner/research-lab"
base_branch = "main"
branch_prefix = "run/"
private = true
EOF
fresh_start "$T/START3.md"
if out=$(AAR_PROFILE="$T/xdg/experiment-lifecycle/aar-profile-v2.toml" bash "$SCRIPT" snapshot "$T/START3.md" 2>&1); then
  err "snapshot succeeded against an unknown schema_version=2 profile"
else
  case "$out" in *"understands only 1"*) ok "unknown schema_version MAJOR BLOCKs (refuse-unknown-MAJOR)";;
    *) err "unknown-MAJOR failure lacked the expected message: $out";; esac
fi

# 6. profile missing a required [github] field -> BLOCKs.
cat > "$T/xdg/experiment-lifecycle/aar-profile-badgh.toml" <<'EOF'
schema_version = 1
[github]
research_repo = "owner/research-lab"
base_branch = "main"
private = true
EOF
fresh_start "$T/START4.md"
if out=$(AAR_PROFILE="$T/xdg/experiment-lifecycle/aar-profile-badgh.toml" bash "$SCRIPT" snapshot "$T/START4.md" 2>&1); then
  err "snapshot succeeded with [github].branch_prefix missing"
else
  case "$out" in *"branch_prefix"*) ok "missing required [github] field BLOCKs";;
    *) err "missing-field failure lacked the expected message: $out";; esac
fi

# 7. viewer-less profile -> snapshot succeeds manifest-only, check agrees ([recipes.viewer] legitimately absent).
mkdir -p "$T/xdg-viewerless/experiment-lifecycle"
viewerless_profile "$T/xdg-viewerless/experiment-lifecycle/aar-profile.toml"
fresh_start "$T/START5.md"
if out=$(XDG_CONFIG_HOME="$T/xdg-viewerless" bash "$SCRIPT" snapshot "$T/START5.md" 2>&1) \
  && printf '%s' "$out" | grep -q 'manifest-only' && ! grep -q '\[recipes.viewer\]' "$T/START5.md"; then
  ok "viewer-less profile snapshots manifest-only, with no [recipes.viewer] table written"
else
  err "viewer-less profile snapshot did not behave as manifest-only: $out"
fi
if out=$(XDG_CONFIG_HOME="$T/xdg-viewerless" bash "$SCRIPT" check "$T/START5.md" 2>&1); then
  ok "check passes on a legitimate viewer-less (manifest-only) snapshot"
else
  err "check failed on a legitimate viewer-less snapshot: $out"
fi

# 8. check on a START.md with no snapshot block at all -> BLOCKs with the expected pointer.
printf '# no snapshot section here\n' > "$T/START6.md"
if out=$(bash "$SCRIPT" check "$T/START6.md" 2>&1); then
  err "check succeeded with no instance-profile snapshot block in START.md at all"
else
  case "$out" in *"no instance-profile snapshot block found"*"snapshot' first"*) ok "missing snapshot block BLOCKs with a clear pointer";;
    *) err "missing-block failure lacked the expected message: $out";; esac
fi

# 9. tampered snapshot: [recipes.viewer] hand-removed from the block while profile_sha256 is left matching
#    the (unchanged) live profile -> the presence cross-check catches it independently of the hash check.
fresh_start "$T/START7.md"
bash "$SCRIPT" snapshot "$T/START7.md" >/dev/null 2>&1
python3 - "$T/START7.md" <<'PY'
import re, sys
p = sys.argv[1]
text = open(p).read()
text = re.sub(r"\n\[recipes\.viewer\]\n(?:.*\n)*?```\n", "\n```\n", text, count=1)
open(p, "w").write(text)
PY
if out=$(bash "$SCRIPT" check "$T/START7.md" 2>&1); then
  err "check passed despite [recipes.viewer] being hand-stripped from an otherwise-matching snapshot"
else
  case "$out" in *"presence"*"disagrees"*) ok "hand-tampered [recipes.viewer] presence mismatch is caught independently of the hash check";;
    *) err "tampered-presence failure lacked the expected message: $out";; esac
fi

# 10. hardcoded-instance-path absence: static grep over the NEW files this issue ships (the helper +
#     this smoke script itself is excluded, since its own source necessarily quotes the forbidden
#     literals as toy fixtures/patterns). Scoped to scripts/ (not the whole skill dir) so this check does
#     not flag pre-existing, unrelated example prose elsewhere in the skill (e.g. SCHEMA.md's own
#     "owner/research-lab" placeholder examples, which predate and are out of scope for this change).
LEAK_PATTERN='/home/anton|cloudflare|\.trycloudflare\.com|:[0-9]{4,5}\b'
SCRIPTS_DIR="$ROOT/plugins/experiment-lifecycle/skills/design-experiment/scripts"
leaked=$(grep -RinE "$LEAK_PATTERN" "$SCRIPTS_DIR" --include='*.sh' \
  2>/dev/null | grep -v '/aar_profile_snapshot_smoke\.sh:' || true)
if [ -n "$leaked" ]; then
  err "instance-specific value(s) found in the new aar_profile_snapshot files:"
  printf '%s\n' "$leaked" >&2
else
  ok "no hardcoded home-anton/hostname-port/cloudflare values in the new aar_profile_snapshot files"
fi

[ "$fail" = 0 ] && { echo "  smoke ok: aar_profile_snapshot snapshot/check round-trip + staleness + fail-closed + manifest-only + tamper detection" >&2; exit 0; } || exit 1
